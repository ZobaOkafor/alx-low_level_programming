#include <stdio.h>


/**
 * add - add two numbers
 * @a: integer one
 * @b: integer two
 *
 * Return: result
 */

int add(int a, int b)
{
	return (a + b);
}


/**
 * sub - subtract two numbers
 * @a: integer one
 * @b: integer two
 *
 * Return: result
 */

int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - multiply two numbers
 * @a: integer one
 * @b: integer two
 *
 * Return: result
 */

int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - divide two numbers
 * @a: integer one
 * @b: integer two
 *
 * Return: result
 */

int div(int a, int b)
{
	return (a / b);
}


/**
 * mod - mod of two numbers
 * @a: integer one
 * @b: integer two
 *
 * Return: result
 */

int mod(int a, int b)
{
	return (a % b);
}
